import type { LangFilter } from "@/types/search";

/**
 * Episode item returned by POST /api/search/episodes (v2).
 * Corresponds to EpisodeDto in podcast-spec.
 */
export interface EpisodeDto {
  episodeId: string;
  title: string;
  description: string;
  highlights: { title?: string[]; description?: string[] };
  publishedAt: string;      // ISO 8601
  durationSec: number;
  imageUrl: string;
  /** Language index this episode belongs to: "zh-tw" | "zh-cn" | "en" */
  language: string;
  podcast: {
    podcastId: string;
    title: string;
    publisher: string;
    imageUrl: string;
  };
}

/**
 * Response type for POST /api/search/episodes (v2).
 * Corresponds to EpisodeSearchResponse in podcast-spec.
 *
 * searchRequestId: UUID generated by the backend per search request.
 * The frontend must pass it back in subsequent click log events
 * to enable query log / click log join. Do not generate a new UUID on the frontend.
 */
export interface EpisodeSearchResponse {
  status: "ok";
  searchRequestId: string;
  data: {
    page: number;
    size: number;
    total: number;
    items: EpisodeDto[];
  };
}

// Re-export LangFilter here for convenience when constructing search requests
export type { LangFilter };
